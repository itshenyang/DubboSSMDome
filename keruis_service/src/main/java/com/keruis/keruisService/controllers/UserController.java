package com.keruis.keruisService.controllers;

import com.keruis.keruisService.services.UserServiceImp;
import com.keruis.keruisService.utils.*;
import com.keruis.keruis_pojo.Pojos.User;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.util.StringUtils;
import org.springframework.web.bind.annotation.*;

/**
 * Created by Administrator on 2017/3/20.
 */
@Controller
@RequestMapping(value = "user")
public class UserController extends BaseController {
    @Autowired
    UserServiceImp userServiceImp;

    /**
     * 登录验证
     *
     * @param user
     * @return
     */
    @RequestMapping(value = "login", method = RequestMethod.POST)
    @ResponseBody
    public Object login(@RequestBody User user) {
        L.w(user.toString());

         return userServiceImp.Login(user);
    }



}
